    public void setDuLieuID_TKDT(){
        ArrayList<DoanhThu> list = new ArrayList<DoanhThu>();
        float Tong = 0;
        cbNam_TKDT.addItem("Tất cả");
        try{
            list = DoanhThuDao.Select();
            for(int i = 0;i < list.size();i++){
                Boolean check = true;
                Date d = new SimpleDateFormat("yyyy-MM-dd").parse(list.get(i).getNgayDongHP());
                for(int j = 0;j < cbNam_TKDT.getItemCount();j++){
                    if(String.valueOf(cbNam_TKDT.getItemAt(j)).equals(String.valueOf(d.getYear()+1900))){
                        check = false;
                        break;
                    }
                }
                if(check == true){
                    cbNam_TKDT.addItem(String.valueOf(d.getYear()+1900));
                }
                Tong = Tong + list.get(i).getHocPhi();
            }
            txtTongDT_TKDT.setText(String.valueOf(Tong));
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
    public void nam_TKDT(){
        String nam = String.valueOf(cbNam_TKDT.getSelectedItem());
        float Tong = 0;
        if(nam.equals("Tất cả")){
            DefaultTableModel tb = (DefaultTableModel) tb_TKDT.getModel();
            for(int i = tb.getRowCount() - 1;i >= 0; i--){
                tb.removeRow(i);
            }
            setDuLieu_TKDT();
        }else{
            DefaultTableModel tb = (DefaultTableModel) tb_TKDT.getModel();
            for(int i = tb.getRowCount() - 1;i >= 0; i--){
                tb.removeRow(i);
            }
            ArrayList<DoanhThu> list = new ArrayList<DoanhThu>();
            try{
                list = DoanhThuDao.Select();
                for(int i = 0;i < list.size();i++){
                    Date d = new SimpleDateFormat("yyyy-MM-dd").parse(list.get(i).getNgayDongHP());
                    if(String.valueOf(d.getYear() + 1900).equals(nam)){
                        Object[] row = {
                            list.get(i).getIDHV(), list.get(i).getHocPhi(), list.get(i).getNgayDongHP()
                        };
                        tb.addRow(row);
                        Tong += list.get(i).getHocPhi();
                    }
                }
                txtTongDT_TKDT.setText(String.valueOf(Tong));
            }catch(Exception ex){
                ex.printStackTrace();
            }
            
        }
        
    }

