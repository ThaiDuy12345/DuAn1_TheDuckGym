    public void hienThi_QLTK(){
        cbID_QLTK.setEnabled(false);
        DefaultTableModel tb = (DefaultTableModel) tb_QLTK.getModel();
        int r = tb_QLTK.getSelectedRow();
        String ID = String.valueOf(tb.getValueAt(r,0));
        ArrayList<TaiKhoan> list = new ArrayList<TaiKhoan>();
        try{
            list = TaiKhoanDao.Select();
            for(int i = 0;i < list.size();i++){
                if(list.get(i).getID().equals(ID)){
                    cbID_QLTK.setSelectedItem(list.get(i).getID());
                    txtMatKhau_QLTK.setText(list.get(i).getMatKhau());
                    if(list.get(i).getVaiTro().equals("Quản lý")){
                        rbQuanLy_QLTK.setSelected(true);
                        rbQuanLy_QLTK.setEnabled(true);
                        rbNhanVien_QLTK.setEnabled(true);
                        rbHocVien_QLTK.setEnabled(false);
                    }else if(list.get(i).getVaiTro().equals("Nhân viên")){
                        rbNhanVien_QLTK.setSelected(true);
                        rbQuanLy_QLTK.setEnabled(true);
                        rbNhanVien_QLTK.setEnabled(true);
                        rbHocVien_QLTK.setEnabled(false);
                    }else{
                        rbHocVien_QLTK.setSelected(true);
                        rbHocVien_QLTK.setEnabled(true);
                        rbNhanVien_QLTK.setEnabled(false);
                        rbQuanLy_QLTK.setEnabled(false);
                    }
                    break;
                }
            }
        }catch(Exception ex){
           ex.printStackTrace();
        }
    }  
